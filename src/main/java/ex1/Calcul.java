public class Calcul {

	/** Méthode qui retourne l'addition des paramètres a et b
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/** Méthode qui retourne la soustraction de b à a
	 * @param a paramètre a
	 * @param b paramètre b
	 * @return int
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
